#!/bin/sh

D_ENABLE_PIXEL_CNTR_=$(grep D_ENABLE_PIXEL_CNTR_ ../CFLAGS|cut -d'=' -f2)
D_ENABLE_LINE_CNTR_=$(grep D_ENABLE_LINE_CNTR_ ../CFLAGS|cut -d'=' -f2)
D_ENABLE_FRAME_CNTR_=$(grep D_ENABLE_FRAME_CNTR_ ../CFLAGS|cut -d'=' -f2)
D_DEPTH_=$(grep D_DEPTH_ ../CFLAGS|cut -d'=' -f2)
D_PPC_=$(grep D_PPC_ ../CFLAGS|cut -d'=' -f2)
D_ID_=$(cat ../ID)
D_TOP_=$(cat ../TOP)_Id${D_ID_}

CFLAGS=""
CFLAGS="$CFLAGS -DD_ENABLE_PIXEL_CNTR_=$D_ENABLE_PIXEL_CNTR_"
CFLAGS="$CFLAGS -DD_ENABLE_LINE_CNTR_=$D_ENABLE_LINE_CNTR_"
CFLAGS="$CFLAGS -DD_ENABLE_FRAME_CNTR_=$D_ENABLE_FRAME_CNTR_"
CFLAGS="$CFLAGS -DD_DEPTH_=$D_DEPTH_"
CFLAGS="$CFLAGS -DD_PPC_=$D_PPC_"
CFLAGS="$CFLAGS -DD_TOP_=$D_TOP_"
